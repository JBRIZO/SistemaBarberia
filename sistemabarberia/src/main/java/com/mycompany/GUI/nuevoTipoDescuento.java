    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        try{
        List<tipodescuento> descuentosEnBd = tipodescuentoDAO.findtipodescuentoEntities();
        tipodescuento tipoDescuentoNuevo = new tipodescuento();
        tipoDescuentoNuevo.setNomDescuento(tipoDescuento.getText());
        tipoDescuentoNuevo.setActivo(true);
        
        if(tipoDescuento.getText().equals("")||tipoDescuento.getText().equals("Nombre Tipo de Descuento"))
        {
            JOptionPane.showMessageDialog(this,"Debes ingresar un tipo de descuento","Campos sin rellenar",JOptionPane.ERROR_MESSAGE);
            return;
        }
        for(int i=0; i < descuentosEnBd.size();i++)
        {
            if(tipoDescuentoNuevo.getNomDescuento().equalsIgnoreCase(descuentosEnBd.get(i).getNomDescuento()))
            {
                Border border = BorderFactory.createLineBorder(Color.RED, 1);
                tipoDescuento.setBorder(border);
                formatoInvalido.setVisible(true);
                formatoInvalido.setText("Ese tipo de descuento ya existe.");
                return;
            }
        }
       
        if(validacionCampos()){
            //modificar
            if(modificar)
            {
                try {
                    tipoDescuentoNuevo.setIdtipodescuento(descuentoModificar.getIdtipodescuento());
                    tipoDescuentoNuevo.setActivo(true);
                    tipodescuentoDAO.edit(tipoDescuentoNuevo);
                    JOptionPane.showMessageDialog(null,"Operaci√≥n Exitosa");
                    return;
                    } catch (Exception ex) {
                            JOptionPane.showMessageDialog(null,"No se pudo guardar, excepci√≥n: " + ex.getMessage());
                            return;
                    }
            }
            try {
            tipodescuentoDAO.create(tipoDescuentoNuevo);
            JOptionPane.showMessageDialog(null,"Operaci√≥n Exitosa");
                    Reiniciar();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"No se pudo guardar, excepci√≥n: " + ex.getMessage());
        }
        }else{JOptionPane.showMessageDialog(null, "Por favor, corrige los campos en rojo.","Datos inv√°lidos",JOptionPane.ERROR_MESSAGE);}
        }catch(Exception ex){
            log(ex);
        }
    }//GEN-LAST:event_botonAceptarActionPerformed

    
    
